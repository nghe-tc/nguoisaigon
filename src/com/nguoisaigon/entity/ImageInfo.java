package com.nguoisaigon.entity;

public class ImageInfo {

	private String imageId;
	private String productId;
	private String imageUrl;
	private String product_image_data;

	public String getImageId() {
		return imageId;
	}

	public void setImageId(String imageId) {
		this.imageId = imageId;
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	public String getProduct_image_data() {
		return product_image_data;
	}

	public void setProduct_image_data(String product_image_data) {
		this.product_image_data = product_image_data;
	}
}
