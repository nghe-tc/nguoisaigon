package com.nguoisaigon.entity;

public class SettingInfo {

	private String appLink;
	private String parseAppId;
	private String settingId;

	/**
	 * @return the appLink
	 */
	public String getAppLink() {
		return appLink;
	}

	/**
	 * @param appLink
	 *            the appLink to set
	 */
	public void setAppLink(String appLink) {
		this.appLink = appLink;
	}

	/**
	 * @return the parseAppId
	 */
	public String getParseAppId() {
		return parseAppId;
	}

	/**
	 * @param parseAppId
	 *            the parseAppId to set
	 */
	public void setParseAppId(String parseAppId) {
		this.parseAppId = parseAppId;
	}

	/**
	 * @return the settingId
	 */
	public String getSettingId() {
		return settingId;
	}

	/**
	 * @param settingId
	 *            the settingId to set
	 */
	public void setSettingId(String settingId) {
		this.settingId = settingId;
	}

}
